#include <common.h>
#include <utils.h>

#define TIMER_HZ 60

void init_map();
void init_serial();
void init_timer();

void device_update() {
  static uint64_t last = 0;
  uint64_t now = get_time();
  if (now - last < 1000000 / TIMER_HZ) {
    return;
  }
  last = now;
}

void init_device() {
  init_map();

  init_serial();
  init_timer();
}
